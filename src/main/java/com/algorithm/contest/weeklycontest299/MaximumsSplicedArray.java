    public static int maximumsSplicedArray(int[] nums1, int[] nums2) {
        return Math.max(helper(nums1, nums2), helper(nums2, nums1));
    }

    public static int helper(int[] nums1, int[] nums2) {
        int n = nums1.length;
        int sum = 0, maxDiff = 0, sub = 0;
        for (int i = 0; i < n; i++) {
            sum += nums1[i];
            sub = Math.max(sub + nums2[i] - nums1[i], 0);
            maxDiff = Math.max(maxDiff, sub);
        }
        return sum + maxDiff;
    }
