    public int chalkReplacer(int[] chalk, int k) {
        long sum = 0;
        for (int c : chalk) {
            sum += c;
        }

        k = (int)(k % sum);

        if (k == 0) {
            return 0;
        }

        int n = chalk.length;
        for (int i = 0; i < n; i++) {
            if (k >= chalk[i]) {
                k -= chalk[i];
                continue;
            }
            return i;
        }
        return 0;
    }
